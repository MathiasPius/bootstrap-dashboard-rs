//! Font-Awesome icons
use std::{borrow::Cow, fmt::Display};

/// Font-Awesome Icon
pub struct Icon(Cow<'static, str>);

impl Icon {
    /// If the icon does not appear in th [`fa`] module, you can specify one manually.
    pub fn custom(name: &str) -> Icon {
        Icon(name.to_string().into())
    }
}

impl Display for Icon {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        f.write_str(self.0.as_ref())
    }
}

